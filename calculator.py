import math

def fatorial(n):
    return math.factorial(n)

def exponenciacao(base, expoente):
    return base ** expoente

def logaritmo(numero, base):
    return math.log(numero, base)

def soma(a, b):
    return a+b

def subtracao(a, b):
    return a-b

def soma_de_quadrados(a, b):
    return a**2 + b**2
